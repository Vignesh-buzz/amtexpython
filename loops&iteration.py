#using for loop
x=[11,23,45,56,3,12,89]
for num in x:
    if(num==45):
        print("found!")
        break
    print(num)
    
    
#using continue function
x = [11, 23, 45, 56, 3, 12, 89]
for num in x:
    if (num == 45):
        print("found!")
        continue
    print(num)


